package example3;

public class Number {
	private int count = 0;
	private int number;
	
	public Number(int number) {
		super();
		this.number = number;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}

}


